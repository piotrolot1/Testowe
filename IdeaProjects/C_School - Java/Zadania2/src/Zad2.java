public class Zad2 {
    public static void main(String[] args) {

        int j = 1;

        do {
            int i = 1;
            do {
                System.out.println(j + " * " + i + " = " + (j * i));
                i++;
            }while (i <= 10);
            j++;
        }while (j <= 10);
        }
    }
// Napisz program z wykorzystaniem pętli do-while wyświetlający tabliczkę mnożenia Do 100




