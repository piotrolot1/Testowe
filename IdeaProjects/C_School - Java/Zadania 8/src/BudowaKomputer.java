
class BudowaKom {
    private String Myszka;
    private String Monitor;
    private String Klawiatura;
    private int Wiek;



    public BudowaKom(String myszka, String monitor, String klawiatura, int wiek) {

        
        this.Myszka = myszka;
        this.Monitor = monitor;
        this.Klawiatura = klawiatura;
        this.Wiek = wiek;
    }
    public void setMyszka(String myszka) {
        Myszka = myszka;
    }

    public void setMonitor(String monitor) {
        Monitor = monitor;
    }

    public void setKlawiatura(String klawiatura) {
        Klawiatura = klawiatura;
    }

    public void setWiek(int wiek) {
        Wiek = wiek;
    }
}
