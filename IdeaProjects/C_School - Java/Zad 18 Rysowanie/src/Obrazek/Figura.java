package Obrazek;

import java.awt.Color;

public abstract class Figura {
    protected int x;
    protected int y;
    protected int width;
    protected int height;
    protected Color color;

    public Figura(int x, int y, int width, int height, Color color) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.color = color;
    }

    public Color getColor() {
        return color;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
